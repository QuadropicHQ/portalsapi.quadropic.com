function debugPrint(info) {
  console.debug(info);
  return info;
}

function errorPrint(info) {
  console.error(info);
  return info;
}

function infoPrint(info) {
  console.info(info);
  return info;
}

function logPrint(info) {
  console.log(info);
  return info;
}

function warnPrint(info) {
  console.warn(info);
  return info;
}
